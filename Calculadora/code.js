class Calculadora {

  somar(firstNumber, secondNumber) {
    return firstNumber + secondNumber
  }

  subtrair(firstNumber, secondNumber) {
    return firstNumber - secondNumber
  }

  multiplicar(firstNumber, secondNumber) {
    return firstNumber * secondNumber
  }

  dividir(firstNumber, secondNumber) {
    return firstNumber / secondNumber
  }
}


module.exports = Calculadora